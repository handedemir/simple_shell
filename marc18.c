		return (free(potr), NULL);
	if (new_size == old_size)
		return (potr);

	p = malloc(new_size);
	if (!p)
		return (NULL);

	old_size = old_size < new_size ? old_size : new_size;
	while (old_size--)
		p[old_size] = ((char *)potr)[old_size];
